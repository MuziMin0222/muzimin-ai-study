str = "0123456789"

# 切片：str[开始位置:结束位置:步长]，字符串，元组，列表都支持，字典不支持切片

# 截取从2—5位置的字符串
print(str[2:6])  # 2345

# 截取从2 ～ 末尾的字符串
print(str[2:])  # 23456789

# 截取从开始 ～ 5位置的字符串
print(str[:6])  # 012345

# 截取完整的字符串
print(str[:])  # 0123456789

# 从开始位置，每隔一个字符串截取字符串
print(str[::2])  # 02468

# 从索引1开始，每隔一个字符串截取一个
print(str[1::2])  # 13579

# 截取从2 ～ 末尾 -1的字符串
print(str[2:-1])  # 2345678

# 截取字符串末尾两个字符
print(str[-2:])

# 字符串的逆序
print(str[-1::-1])  # 9876543210
print(str[::-1])  # 9876543210
